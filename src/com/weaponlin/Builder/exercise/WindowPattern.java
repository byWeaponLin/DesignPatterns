public class WindowPattern {
    private String menu;
    private String playList;
    private String mainWindow;
    private String controlStrip;
    private String favoList;

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }

    public String getPlayList() {
        return playList;
    }

    public void setPlayList(String playList) {
        this.playList = playList;
    }

    public String getMainWindow() {
        return mainWindow;
    }

    public void setMainWindow(String mainWindow) {
        this.mainWindow = mainWindow;
    }

    public String getControlStrip() {
        return controlStrip;
    }

    public void setControlStrip(String controlStrip) {
        this.controlStrip = controlStrip;
    }

    public String getFavoList() {
        return favoList;
    }

    public void setFavoList(String favoList) {
        this.favoList = favoList;
    }
}
